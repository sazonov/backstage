package com.proit.app.configuration;

import org.springframework.beans.factory.annotation.Qualifier;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.jdbc.datasource.DataSourceTransactionManager;
import org.springframework.transaction.PlatformTransactionManager;
import org.springframework.transaction.annotation.EnableTransactionManagement;
import org.springframework.transaction.support.TransactionTemplate;

import javax.sql.DataSource;

@Configuration
@EnableTransactionManagement
public class DictsTransactionConfiguration
{
	@Bean
	public PlatformTransactionManager dictTransactionManager(@Qualifier("dictDataSource") DataSource dataSource)
	{
		return new DataSourceTransactionManager(dataSource);
	}

	@Bean
	public TransactionTemplate dictTransactionTemplate(@Qualifier("dictTransactionManager") PlatformTransactionManager manager)
	{
		return new TransactionTemplate(manager);
	}
}
